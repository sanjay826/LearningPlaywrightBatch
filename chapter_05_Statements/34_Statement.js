/**
 * ==========================================
 * JavaScript Statements (if / else)
 * ==========================================
 *
 * The `if / else` statement is JavaScript's fundamental decision-making tool.
 * It executes a block of code if the specified condition evaluates to `true`,
 * and optionally runs a different block of code if the condition is `false`.
 * We can also chain multiple conditions using `else if`.
 */

// Example: Using if-else statements
let age = 28;

if (age > 18) {
    console.log("Yes Goa!!");
} else {
    console.log("Go Home!!");
}
