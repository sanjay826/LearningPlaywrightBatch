/**
 * LOGICAL OPERATORS
 * 
 * Logical operators are used to combine or manipulate boolean values.
 * They evaluate expressions and return true or false based on logical conditions.
 * 
 * Types:
 * 1. AND (&&)  - Returns true only if both operands are true
 * 2. OR  (||)  - Returns true if at least one operand is true
 * 3. NOT (!)   - Reverses/inverts the boolean value
 */

let a = true;
let b = false;

// AND Operator - Both conditions must be true
console.log(a && b); // false

// OR Operator - At least one condition must be true
console.log(a || b); // true

// NOT Operator - Inverts the boolean value
console.log(!a); // false
