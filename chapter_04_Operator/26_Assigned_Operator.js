
/**
 * Arithmetic Operators
 * Arithmetic operators are used to perform mathematical calculations on numbers.
 * These operators take two operands and return a numerical result.
 * 
 * Supported Arithmetic Operators:
 * - Addition (+): Adds two numbers together
 * - Subtraction (-): Subtracts the right operand from the left operand
 * - Multiplication (*): Multiplies two numbers
 * - Division (/): Divides the left operand by the right operand
 * - Modulus (%): Returns the remainder of a division operation
 * - Exponentiation (**): Raises the left operand to the power of the right operand
 * 
 * @example
 * let a = 10;
 * let b = 3;
 * a + b;  // 13 (addition)
 * a - b;  // 7 (subtraction)
 * a * b;  // 30 (multiplication)
 * a / b;  // 3.333... (division)
 * a % b;  // 1 (modulus)
 * a ** b; // 1000 (exponentiation)
 * 
 * @returns {number} The result of the arithmetic operation
 */
/**
 * Assignment Operator (=)
 * The assignment operator (=) is used to assign a value to a variable.
 * It assigns the value on the right side to the variable on the left side.
 * 
 * @type {string}
 */
// Variable declarations
let a = 10;
let b = 3;

// Arithmetic Operators: addition, subtraction, multiplication
let sum = a + b;        // Example: 10 + 3 = 13
let sub = a - b;        // Example: 10 - 3 = 7
let mul = a * b;        // Example: 10 * 3 = 30
console.log(sum);       // Output: 13
console.log(sub);       // Output: 7
console.log(mul);       // Output: 30

// Modulus Operator - returns the remainder of division
console.log(a % b);     // Example: 10 % 3 = 1
console.log(13 % 7);    // Example: 13 % 7 = 6
console.log(100 % 2);   // Example: 100 % 2 = 0 (even number)
console.log(101 % 2);   // Example: 101 % 2 = 1 (odd number)
console.log(103 % 2);   // Example: 103 % 2 = 1 (odd number)

// Exponentiation Operator: a^b
console.log(2 ** 3);    // Example: 2^3 = 8 (2*2*2)
console.log(a ** b);    // Example: 10^3 = 1000

